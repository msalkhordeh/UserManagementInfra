namespace UM.DataAccess.Enum
{
    /// <summary>
    /// Collection of the Gender for any user
    /// </summary>
    public enum Gender : byte
    {
        /// <summary>
        /// Represent a Male Gender value for user
        /// </summary>
        Male = 0,

        /// <summary>
        /// Represent a Female Gender value for user
        /// </summary>
        Female = 1,

        /// <summary>
        /// Represent an Unknown Gender for user
        /// </summary>
        NoInterested = 2
    }
}
